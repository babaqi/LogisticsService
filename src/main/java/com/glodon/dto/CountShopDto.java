package com.glodon.dto;

public class CountShopDto {
	public String shopName;
	public String shopArriveOrder;
	public String shopProblemOrder;
	public String shopPrice;
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getShopArriveOrder() {
		return shopArriveOrder;
	}
	public void setShopArriveOrder(String shopArriveOrder) {
		this.shopArriveOrder = shopArriveOrder;
	}
	public String getShopProblemOrder() {
		return shopProblemOrder;
	}
	public void setShopProblemOrder(String shopProblemOrder) {
		this.shopProblemOrder = shopProblemOrder;
	}
	public String getShopPrice() {
		return shopPrice;
	}
	public void setShopPrice(String shopPrice) {
		this.shopPrice = shopPrice;
	}

	
}
