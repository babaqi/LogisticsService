package com.glodon.dto;

public class CountUserDto {
	public String userName;
	public String userArriveOrder;
	public String userProblemOrder;
	public String userPrice;

	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserArriveOrder() {
		return userArriveOrder;
	}
	public void setUserArriveOrder(String userArriveOrder) {
		this.userArriveOrder = userArriveOrder;
	}
	public String getUserProblemOrder() {
		return userProblemOrder;
	}
	public void setUserProblemOrder(String userProblemOrder) {
		this.userProblemOrder = userProblemOrder;
	}
	public String getUserPrice() {
		return userPrice;
	}
	public void setUserPrice(String userPrice) {
		this.userPrice = userPrice;
	}

}
